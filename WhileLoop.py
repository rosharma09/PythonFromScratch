# set the counter variable

i = 10

while i >= 1: # put the condition
    if(i == 1):
        print(str(i)+'st Iteration')
    elif(i==2):
        print(str(i)+'nd Iteration')
    elif(i==3):
        print(str(i)+'rd Iteration')
    else:
        print(str(i)+'th Iteration')

    i -= 1 # increment/decrement the counter

